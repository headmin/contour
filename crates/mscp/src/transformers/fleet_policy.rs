    #[test]
    fn test_odv_in_title_resolved() {
        let info = yaml_serde::to_value(serde_json::json!({
            "com.apple.screensaver": {
                "idleTime": "$ODV"
            }
        }))
        .unwrap();

        let odv = yaml_serde::to_value(serde_json::json!({
            "hint": "Max idle time in seconds",
            "recommended": 1200,
            "cis_lvl2": 60
        }))
        .unwrap();

        let mut rule = make_rule(
            "screensaver_odv_title",
            "Restrict Screen Saver Timeout to $ODV Seconds",
            Some(info),
        );
        rule.odv = Some(odv);

        let generator = FleetPolicyGenerator::new("cis_lvl2");
        let policy = generator.generate_policy_for_rule(&rule, "cis_lvl2", None);

        assert!(policy.is_some());
        let p = policy.unwrap();
        // Title should have $ODV replaced with the resolved value
        assert!(
            !p.name.contains("$ODV"),
            "name still contains $ODV: {}",
            p.name
        );
        assert!(
            !p.description.contains("$ODV"),
            "description still contains $ODV: {}",
            p.description
        );
        assert!(
            p.name.contains("60"),
            "name should contain resolved value 60: {}",
            p.name
        );
    }

