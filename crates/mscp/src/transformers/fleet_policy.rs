    fn make_rule(id: &str, title: &str, mobileconfig_info: Option<yaml_serde::Value>) -> MscpRule {
        MscpRule {
            id: id.to_string(),
            title: title.to_string(),
            discussion: String::new(),
            check: None,
            result: None,
            fix: None,
            references: HashMap::new(),
            macos: vec![],
            tags: vec![],
            severity: None,
            mobileconfig: mobileconfig_info.is_some(),
            mobileconfig_info,
            odv: None,
        }
    }

    #[test]
    fn test_simple_boolean_policy() {
        let info = yaml_serde::to_value(serde_json::json!({
            "com.apple.screensaver": {
                "askForPassword": true
            }
        }))
        .unwrap();

        let rule = make_rule(
            "screensaver_ask",
            "Enforce Screen Saver Password",
            Some(info),
        );
        let generator = FleetPolicyGenerator::new("cis_lvl1");
        let policy = generator.generate_policy_for_rule(&rule, "cis_lvl1", None);

        assert!(policy.is_some());
        let p = policy.unwrap();
        assert_eq!(p.platform, "darwin");
        assert!(p.query.contains("managed_policies"));
        assert!(p.query.contains("com.apple.screensaver"));
        assert!(p.query.contains("askForPassword"));
        assert!(p.query.contains("'1'"));
    }

