//! Fleet `GitOps` structure generator
//!
//! Generates a complete Fleet `GitOps` directory structure matching the official
//! fleet-gitops reference: <https://github.com/fleetdm/fleet-gitops>
//!
//! Uses `FleetLayout` for all path conventions (Fleet v4.82+).

use crate::config::GlobSection;
use crate::models::{
    AgentConfig, AgentConfigOptions, AgentOptions, AgentOptionsRef, Controls, CustomSetting,
    Decorators, EnrollSecret, Features, FleetGlobalConfig, FleetTeamConfig, OrgInfo, OrgSettings,
    PlatformSettings, Script, ServerSettings, Software, TeamSettings,
};
use anyhow::Result;
use contour_core::fleet_layout::FleetLayout;
use std::path::{Path, PathBuf};

/// Per-team glob decisions threaded from `mscp.toml` into team YAML emission.
///
/// `None` for a section means "no glob — emit one literal `path:` per item,
/// same as before the feature existed". `Some` means "emit a single `paths:`
/// entry plus one literal `path:` per exception".
#[derive(Debug, Clone, Default)]
pub struct GlobPlan<'a> {
    pub profiles: Option<&'a GlobSection>,
    pub scripts: Option<&'a GlobSection>,
}


/// Generator for complete Fleet `GitOps` directory structure
#[derive(Debug)]
pub struct FleetGitOpsGenerator {
    output_base: PathBuf,
    layout: FleetLayout,
}

impl FleetGitOpsGenerator {
    /// Create a new generator with the given output base and layout.
    pub fn new<P: AsRef<Path>>(output_base: P, layout: FleetLayout) -> Self {
        Self {
            output_base: output_base.as_ref().to_path_buf(),
            layout,
        }
    }

    /// Create a new generator with the default Fleet v4.82+ layout.
    pub fn new_default<P: AsRef<Path>>(output_base: P) -> Self {
        Self::new(output_base, FleetLayout::default())
    }

    /// Generate the complete Fleet `GitOps` structure
    pub fn generate_structure(&self) -> Result<()> {
        // Create required directories
        std::fs::create_dir_all(self.output_base.join(self.layout.platforms_dir))?;
        std::fs::create_dir_all(self.output_base.join(self.layout.fleets_dir))?;

        // Generate global files
        self.generate_default_yml()?;
        self.generate_agent_options()?;
        self.generate_unassigned_yml()?;

        Ok(())
    }

    /// Generate default.yml with `org_settings` and global configuration
    pub fn generate_default_yml(&self) -> Result<PathBuf> {
        let config = FleetGlobalConfig {
            policies: Some(Vec::new()),
            reports: Some(Vec::new()),
            agent_options: Some(AgentOptionsRef {
                path: format!("./{}", self.layout.agent_options_path),
            }),
            controls: Some(Controls {
                macos_settings: None,
                ios_settings: None,
                scripts: None,
            }),
            org_settings: Some(OrgSettings {
                server_settings: Some(ServerSettings {
                    server_url: "$FLEET_URL".to_string(),
                }),
                org_info: Some(OrgInfo {
                    org_name: "Fleet".to_string(),
                }),
                secrets: Some(vec![EnrollSecret {
                    secret: "$FLEET_GLOBAL_ENROLL_SECRET".to_string(),
                }]),
                features: Some(Features {
                    enable_host_users: Some(true),
                    enable_software_inventory: Some(true),
                }),
            }),
            labels: None, // Labels are added per-baseline via add_label_to_default_yml
        };

        let file_path = self.output_base.join("default.yml");

        let header = format!(
            r#"# Fleet GitOps - Global Configuration (Fleet v4.82+)
#
# For Fleet Free:
# - This file updates policies, reports, agent_options, and controls for all hosts.
#
# For Fleet Premium:
# - This file updates policies and reports that run on all hosts ("All teams").
# - Remove "controls" and add this to your YAML files in {fleets_dir}/ instead.
#
# Generated by contour mscp - https://github.com/macadmins/contour

"#,
            fleets_dir = self.layout.fleets_dir,
        );

        let yaml_content = yaml_serde::to_string(&config)?;
        std::fs::write(&file_path, format!("{header}{yaml_content}"))?;

        tracing::info!("Generated default.yml: {}", file_path.display());
        Ok(file_path)
    }

    /// Generate agent-options.yml with standard osquery configuration
    pub fn generate_agent_options(&self) -> Result<PathBuf> {
        let options = AgentOptions {
            command_line_flags: None,
            config: Some(AgentConfig {
                decorators: Some(Decorators {
                    load: Some(vec![
                        "SELECT uuid AS host_uuid FROM system_info;".to_string(),
                        "SELECT hostname AS hostname FROM system_info;".to_string(),
                    ]),
                }),
                options: Some(AgentConfigOptions {
                    disable_distributed: Some(false),
                    distributed_interval: Some(10),
                    distributed_plugin: Some("tls".to_string()),
                    distributed_tls_max_attempts: Some(3),
                    logger_tls_endpoint: Some("/api/v1/osquery/log".to_string()),
                    pack_delimiter: Some("/".to_string()),
                }),
            }),
        };

        // Build the agent options path from the layout
        let file_path = self.output_base.join(self.layout.agent_options_path);
        if let Some(parent) = file_path.parent() {
            std::fs::create_dir_all(parent)?;
        }

        let header = r"# Fleet GitOps - Agent Options
#
# Shared osquery agent configuration referenced by fleet files.
# See: https://fleetdm.com/docs/configuration/agent-configuration
#
# Generated by contour mscp - https://github.com/macadmins/contour

";

        let yaml_content = yaml_serde::to_string(&options)?;
        std::fs::write(&file_path, format!("{header}{yaml_content}"))?;

        tracing::info!("Generated agent-options.yml: {}", file_path.display());
        Ok(file_path)
    }

    /// Generate the unassigned hosts YAML file (Fleet v4.82+: unassigned.yml)
    pub fn generate_unassigned_yml(&self) -> Result<PathBuf> {
        let fleets_dir = self.output_base.join(self.layout.fleets_dir);
        std::fs::create_dir_all(&fleets_dir)?;

        let file_path = fleets_dir.join(self.layout.unassigned_filename);

        let content = format!(
            r#"# Fleet GitOps - {unassigned_name} Configuration (Fleet v4.82+)
#
# This file updates policies, controls, and software for hosts assigned to "{unassigned_name}."
# To update reports and agent options for these hosts, use default.yml.
#
# Generated by contour mscp - https://github.com/macadmins/contour

name: {unassigned_name}
policies:
controls:
software:
"#,
            unassigned_name = self.layout.unassigned_name,
        );

        std::fs::write(&file_path, content)?;

        tracing::info!(
            "Generated {}: {}",
            self.layout.unassigned_filename,
            file_path.display()
        );
        Ok(file_path)
    }

    /// Deprecated: use `generate_unassigned_yml` instead.
    #[deprecated(note = "renamed to generate_unassigned_yml for Fleet v4.82+")]
    #[allow(
        dead_code,
        reason = "kept as deprecated alias for backwards compatibility"
    )]
    pub fn generate_no_team_yml(&self) -> Result<PathBuf> {
        self.generate_unassigned_yml()
    }

    /// Generate a fleet YAML file for a specific baseline
    ///
    /// # Arguments
    /// * `baseline_name` - Name of the baseline (e.g., `cis_lvl1`)
    /// * `profile_paths` - Paths to mobileconfig files
    /// * `script_paths` - Paths to script files (audit, remediate)
    pub fn generate_team_yml(
        &self,
        baseline_name: &str,
        profile_paths: &[PathBuf],
        script_paths: &[(PathBuf, Option<PathBuf>)], // (audit, remediate)
    ) -> Result<PathBuf> {
        self.generate_team_yml_with_policies(baseline_name, profile_paths, script_paths, None)
    }

    /// Generate a fleet YAML file for a specific baseline, optionally including
    /// a policies path reference.
    pub fn generate_team_yml_with_policies(
        &self,
        baseline_name: &str,
        profile_paths: &[PathBuf],
        script_paths: &[(PathBuf, Option<PathBuf>)],
        policy_path: Option<&Path>,
    ) -> Result<PathBuf> {
        self.generate_team_yml_with_glob_plan(
            baseline_name,
            profile_paths,
            script_paths,
            policy_path,
            &GlobPlan::default(),
        )
    }

    /// Glob-aware variant: collapses items into a single `paths:` glob where
    /// the plan enables it, and emits literal `path:` entries for exceptions.
    ///
    /// When no plan is provided (`GlobPlan::default()`), behaviour is identical
    /// to the legacy per-item emission.
    pub fn generate_team_yml_with_glob_plan(
        &self,
        baseline_name: &str,
        profile_paths: &[PathBuf],
        script_paths: &[(PathBuf, Option<PathBuf>)],
        policy_path: Option<&Path>,
        glob_plan: &GlobPlan<'_>,
    ) -> Result<PathBuf> {
        let fleets_dir = self.output_base.join(self.layout.fleets_dir);
        std::fs::create_dir_all(&fleets_dir)?;

        let team_name = baseline_name.replace('_', "-");
        let label_name = format!("mscp-{baseline_name}");
        let secret_var = format!(
            "FLEET_{}_ENROLL_SECRET",
            baseline_name.to_uppercase().replace('-', "_")
        );

        let custom_settings =
            self.emit_profiles_section(baseline_name, &label_name, profile_paths, glob_plan.profiles)?;
        let scripts = self.emit_scripts_section(baseline_name, script_paths, glob_plan.scripts)?;

        // Build fleet settings as yaml_serde::Value
        let team_settings = TeamSettings {
            secrets: Some(vec![EnrollSecret {
                secret: format!("${secret_var}"),
            }]),
            features: Some(Features {
                enable_host_users: Some(true),
                enable_software_inventory: Some(true),
            }),
        };

        // Build policies -- either a path reference to generated policy file, or empty
        let policies_value: Vec<yaml_serde::Value> = if let Some(p) = policy_path {
            let policy_filename = p.file_name().and_then(|s| s.to_str()).unwrap_or_default();
            let relative_policy_path = format!(
                "../{}/mscp/{baseline_name}/policies/{policy_filename}",
                self.layout.platforms_dir
            );

            let mut map = yaml_serde::Mapping::new();
            map.insert(
                yaml_serde::Value::String("path".to_string()),
                yaml_serde::Value::String(relative_policy_path),
            );
            vec![yaml_serde::Value::Mapping(map)]
        } else {
            Vec::new()
        };

        let config = FleetTeamConfig {
            name: Some(team_name.clone()),
            policies: Some(policies_value),
            reports: Some(Vec::new()),
            agent_options: Some(yaml_serde::to_value(AgentOptionsRef {
                path: format!("../{}", self.layout.agent_options_path),
            })?),
            controls: Some(Controls {
                macos_settings: if custom_settings.is_empty() {
                    None
                } else {
                    Some(PlatformSettings {
                        custom_settings: Some(custom_settings),
                    })
                },
                ios_settings: None,
                scripts: if scripts.is_empty() {
                    None
                } else {
                    Some(scripts)
                },
            }),
            settings: Some(yaml_serde::to_value(&team_settings)?),
            software: Some(Software::default()),
        };

        let file_path = fleets_dir.join(format!("{baseline_name}.yml"));

        let header = format!(
            r"# Fleet GitOps - Fleet Configuration: {team_name} (Fleet v4.82+)
#
# mSCP Baseline: {baseline_name}
# Profiles and scripts are in: {platforms_dir}/mscp/{baseline_name}/
#
# Required environment variables:
#   - ${secret_var}
#
# Generated by contour mscp - https://github.com/macadmins/contour

",
            platforms_dir = self.layout.platforms_dir,
        );

        let yaml_content = yaml_serde::to_string(&config)?;
        std::fs::write(&file_path, format!("{header}{yaml_content}"))?;

        tracing::info!("Generated fleet YAML: {}", file_path.display());
        Ok(file_path)
    }

    /// Emit the `configuration_profiles` entries for a team YAML.
    ///
    /// - If `section` is `None` or `section.enabled == false`, emits one literal
    ///   `path:` entry per profile with the baseline label (legacy behaviour).
    /// - If `section.enabled == true`, emits one `paths:` glob (no labels —
    ///   Fleet forbids labels on glob entries, which is why the interactive
    ///   flow requires `drop_labels = true` to reach this state) plus one
    ///   literal `path:` entry per exception. Exceptions carry the exception's
    ///   own subfolder + labels.
    ///
    /// Invariant: every returned `CustomSetting` has exactly one of
    /// `path`/`paths` set and labels only on `path:` entries.
    fn emit_profiles_section(
        &self,
        baseline_name: &str,
        label_name: &str,
        profile_paths: &[PathBuf],
        section: Option<&GlobSection>,
    ) -> Result<Vec<CustomSetting>> {
        let base_dir = format!(
            "../{}/mscp/{baseline_name}/profiles",
            self.layout.platforms_dir
        );

        let Some(section) = section.filter(|s| s.enabled) else {
            // Legacy path: one literal per profile, all sharing the baseline label.
            return profile_paths
                .iter()
                .map(|p| {
                    let filename = p.file_name().and_then(|s| s.to_str()).unwrap_or_default();
                    let cs = CustomSetting {
                        path: Some(format!("{base_dir}/{filename}")),
                        paths: None,
                        labels_include_all: Some(vec![label_name.to_string()]),
                        labels_include_any: None,
                        labels_exclude_any: None,
                    };
                    cs.validate()?;
                    Ok(cs)
                })
                .collect();
        };

        if !section.drop_labels {
            anyhow::bail!(
                "configuration_profiles glob enabled for baseline '{baseline_name}' without \
                 drop_labels=true — Fleet does not allow labels on `paths:` entries. \
                 Re-run with --interactive to accept the label-drop, or disable the glob."
            );
        }

        let mut out = Vec::with_capacity(section.exceptions.len() + 1);

        // One `paths:` entry for the flat directory.
        let glob_entry = CustomSetting {
            path: None,
            paths: Some(format!("{base_dir}/*.mobileconfig")),
            labels_include_all: None,
            labels_include_any: None,
            labels_exclude_any: None,
        };
        glob_entry.validate()?;
        out.push(glob_entry);

        // One literal `path:` entry per exception.
        for exc in &section.exceptions {
            let subfolder = exc
                .subfolder
                .as_deref()
                .map(|s| format!("/{s}"))
                .unwrap_or_default();
            let entry = CustomSetting {
                path: Some(format!("{base_dir}{subfolder}/{}", exc.filename)),
                paths: None,
                labels_include_all: non_empty_vec(&exc.labels_include_all),
                labels_include_any: non_empty_vec(&exc.labels_include_any),
                labels_exclude_any: non_empty_vec(&exc.labels_exclude_any),
            };
            entry.validate()?;
            out.push(entry);
        }

        Ok(out)
    }

    /// Emit the `controls.scripts` entries for a team YAML.
    ///
    /// Scripts have no labels in Fleet, so globbing is unconditionally safe —
    /// no `drop_labels` check required.
    fn emit_scripts_section(
        &self,
        baseline_name: &str,
        script_paths: &[(PathBuf, Option<PathBuf>)],
        section: Option<&GlobSection>,
    ) -> Result<Vec<Script>> {
        let base_dir = format!(
            "../{}/mscp/{baseline_name}/scripts",
            self.layout.platforms_dir
        );

        let flatten = |pairs: &[(PathBuf, Option<PathBuf>)]| -> Vec<String> {
            let mut names = Vec::with_capacity(pairs.len() * 2);
            for (audit, remediate) in pairs {
                if let Some(n) = audit.file_name().and_then(|s| s.to_str()) {
                    names.push(n.to_string());
                }
                if let Some(r) = remediate.as_ref()
                    && let Some(n) = r.file_name().and_then(|s| s.to_str())
                {
                    names.push(n.to_string());
                }
            }
            names
        };

        let Some(section) = section.filter(|s| s.enabled) else {
            return flatten(script_paths)
                .into_iter()
                .map(|n| {
                    let s = Script {
                        path: Some(format!("{base_dir}/{n}")),
                        paths: None,
                    };
                    s.validate()?;
                    Ok(s)
                })
                .collect();
        };

        let mut out = Vec::with_capacity(section.exceptions.len() + 1);

        let glob_entry = Script {
            path: None,
            paths: Some(format!("{base_dir}/*.sh")),
        };
        glob_entry.validate()?;
        out.push(glob_entry);

        for exc in &section.exceptions {
            let subfolder = exc
                .subfolder
                .as_deref()
                .map(|s| format!("/{s}"))
                .unwrap_or_default();
            let entry = Script {
                path: Some(format!("{base_dir}{subfolder}/{}", exc.filename)),
                paths: None,
            };
            entry.validate()?;
            out.push(entry);
        }

        Ok(out)
    }

    /// Add a label path reference to default.yml using line-based insertion.
    ///
    /// This preserves comments and formatting by inserting directly into the YAML text
    /// rather than round-tripping through serde.
    pub fn add_label_to_default_yml(&self, label_path: &str) -> Result<()> {
        let file_path = self.output_base.join("default.yml");

        if !file_path.exists() {
            anyhow::bail!("default.yml does not exist. Run generate_structure() first.");
        }

        // Read existing content
        let content = std::fs::read_to_string(&file_path)?;

        // Check if already present
        if content.contains(label_path) {
            tracing::info!("Label reference already present in default.yml: {label_path}");
            return Ok(());
        }

        let entry_line = format!("- path: {label_path}");

        // Try line-based insertion
        let new_content = if let Some(result) = insert_label_entry(&content, &entry_line) {
            result
        } else {
            // Fallback: append labels section at end
            let mut result = content.clone();
            if !result.ends_with('\n') {
                result.push('\n');
            }
            result.push_str(&format!("labels:\n  {entry_line}\n"));
            result
        };

        std::fs::write(&file_path, new_content)?;

        tracing::info!("Added label reference to default.yml: {label_path}");
        Ok(())
    }

    /// Generate a fragment-style default.yml containing only labels section.
    ///
    /// This is used in fragment mode where org_settings, agent_options, and controls
    /// are not included (they come from the target repo).
    pub fn generate_fragment_default_yml(&self, label_paths: &[String]) -> Result<PathBuf> {
        let file_path = self.output_base.join("default.yml");

        let mut content = String::from(
            "# Fleet GitOps - Fragment Configuration (labels only)\n\
             #\n\
             # This fragment provides label definitions to be merged into a target repo.\n\
             #\n\
             # Generated by contour mscp - https://github.com/macadmins/contour\n\
             \n",
        );

        if label_paths.is_empty() {
            content.push_str("labels:\n");
        } else {
            content.push_str("labels:\n");
            for lp in label_paths {
                content.push_str(&format!("  - path: {lp}\n"));
            }
        }

        content.push_str("\nreports:\n\npolicies:\n");

        std::fs::write(&file_path, &content)?;
        tracing::info!("Generated fragment default.yml: {}", file_path.display());
        Ok(file_path)
    }

    /// Generate a `fragment.toml` manifest at the output root.
    pub fn generate_fragment_toml(
        &self,
        name: &str,
        label_paths: &[String],
        profile_entries: &[contour_core::fragment::ProfileEntry],
        policy_entries: &[contour_core::fragment::SimpleEntry],
        lib_files: &[String],
    ) -> Result<PathBuf> {
        let manifest = contour_core::fragment::FragmentManifest {
            fragment: contour_core::fragment::FragmentMeta {
                name: name.to_string(),
                version: "1.0.0".to_string(),
                description: format!("mSCP baseline: {name}"),
                generator: "contour-mscp".to_string(),
            },
            default_yml: contour_core::fragment::DefaultYmlEntries {
                label_paths: label_paths.to_vec(),
                report_paths: Vec::new(),
                policy_paths: policy_entries.iter().map(|e| e.path.clone()).collect(),
            },
            fleet_entries: contour_core::fragment::FleetEntries {
                profiles: profile_entries.to_vec(),
                reports: Vec::new(),
                policies: policy_entries.to_vec(),
                software: Vec::new(),
            },
            lib_files: contour_core::fragment::LibFiles {
                copy: lib_files.to_vec(),
            },
            scripts: contour_core::fragment::ScriptEntries::default(),
        };

        let file_path = self.output_base.join("fragment.toml");
        manifest.save(&file_path)?;
        tracing::info!("Generated fragment.toml: {}", file_path.display());
        Ok(file_path)
    }

    /// Check if default.yml already exists
    pub fn default_yml_exists(&self) -> bool {
        self.output_base.join("default.yml").exists()
    }

    /// Check if agent-options.yml already exists
    #[allow(dead_code, reason = "reserved for future use")]
    pub fn agent_options_exists(&self) -> bool {
        self.output_base
            .join(self.layout.agent_options_path)
            .exists()
    }

    /// Check if unassigned hosts YAML already exists
    #[allow(dead_code, reason = "reserved for future use")]
    pub fn unassigned_yml_exists(&self) -> bool {
        self.output_base
            .join(self.layout.fleets_dir)
            .join(self.layout.unassigned_filename)
            .exists()
    }
}

/// Wrap a `Vec<String>` in `Some` only if it has elements, so empty label
/// lists serialize as "field absent" instead of as an empty YAML sequence.
fn non_empty_vec(v: &[String]) -> Option<Vec<String>> {
    if v.is_empty() {
        None
    } else {
        Some(v.to_vec())
    }
}

/// Insert a label entry line into existing YAML content.
///
/// Finds the `labels:` section and inserts the entry after the last existing
/// `- path:` entry (or right after `labels:` if none exist). Preserves comments
/// and formatting.
fn insert_label_entry(content: &str, entry_line: &str) -> Option<String> {
    let lines: Vec<&str> = content.lines().collect();

    // Find the `labels:` line (top-level, no indentation)
    let labels_idx = lines.iter().position(|l| {
        let trimmed = l.trim();
        trimmed == "labels:" || trimmed.starts_with("labels:")
    })?;

    // Find the indentation to use (look at existing entries under labels:)
    let indent = "  ";

    // Find insertion point: after last `- path:` entry under labels, or right after `labels:`
    let mut insert_idx = labels_idx + 1;
    let mut i = labels_idx + 1;
    while i < lines.len() {
        let line = lines[i];
        let trimmed = line.trim();

        // Stop if we hit another top-level key
        if !trimmed.is_empty() && !trimmed.starts_with('#') && !line.starts_with(' ') {
            break;
        }

        // Track last label entry position
        if trimmed.starts_with("- path:") {
            insert_idx = i + 1;
        }

        i += 1;
    }

    let mut result = Vec::with_capacity(lines.len() + 1);
    for (idx, line) in lines.iter().enumerate() {
        result.push(line.to_string());
        if idx == insert_idx - 1 {
            result.push(format!("{indent}{entry_line}"));
        }
    }

    // If insert_idx is past the end, append
    if insert_idx >= lines.len() {
        result.push(format!("{indent}{entry_line}"));
    }

    Some(result.join("\n") + "\n")
}

